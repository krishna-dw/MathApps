package wishnu.dewabrata.krishna.math

class Math {

    fun add(a:Int, b:Int): Int{
        return a+b
    }

    fun minus(a:Int, b:Int): Int{
        return a-b
    }

    fun multiple(a:Int, b:Int): Int{
        return a * b
    }

    fun divide(a:Int, b:Int): Long{
        return (a as Long / b as Long)
    }
}